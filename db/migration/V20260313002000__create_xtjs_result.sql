CREATE TABLE xtjs_result
(
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY UNIQUE,
    project_identifier_id UUID PRIMARY KEY,
    result                JSONB       NOT NULL DEFAULT '{}'::jsonb,
    create_time           TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time           TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_xtjs_result_project_identifier
        FOREIGN KEY (project_identifier_id)
            REFERENCES xtjs_projects (identifier_id)
            ON DELETE CASCADE
);

COMMENT
ON TABLE xtjs_result IS '项目业务审查结果表';

COMMENT
ON COLUMN xtjs_result.project_identifier_id IS '项目UUID唯一标识，作为主键并外键关联 xtjs_projects.identifier_id';
COMMENT
ON COLUMN xtjs_result.id IS '数据库内部自增数字序号，仅用于内部排序/排查，不作为外键或业务标识';
COMMENT
ON COLUMN xtjs_result.result IS 'JSON 格式审查结果，可动态扩展业务检查项';
COMMENT
ON COLUMN xtjs_result.create_time IS '创建时间';
COMMENT
ON COLUMN xtjs_result.update_time IS '更新时间';

CREATE INDEX idx_xtjs_result_update_time_project
    ON xtjs_result (update_time DESC, project_identifier_id DESC);
